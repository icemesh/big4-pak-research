#include "utils.h"
#include <stdarg.h>
#include <stdio.h>

void MsgErr(const char* fmt, ...)
{
	va_list va;
	char buf[0x400];
	va_start(va, fmt);
	vsprintf_s(buf, 0x400, fmt, va);
	va_end(va);
	puts("ERROR: =====================================================================================================");
	printf("ERROR: ===== %s\n", buf);
	puts("ERROR: =====================================================================================================");
}

void MsgWarn(const char* fmt, ...)
{
	va_list va;
	char buf[0x400];
	va_start(va, fmt);
	vsprintf_s(buf, 0x400, fmt, va);
	va_end(va);
	puts("WARN:  =====================================================================================================");
	printf("WARN:  ===== %s\n", buf);
	puts("WARN:  =====================================================================================================");
}
